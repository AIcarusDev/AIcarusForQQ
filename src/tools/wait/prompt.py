DESCRIPTION = """
重要的等待工具，可以让你暂时挂起，等待。

## 适用场合示例

在社交平台中：

  - 你察觉到对方似乎话没说完，后面可能还有新内容，适合先做倾听状 -> 考虑设定一个较短的等待时间（例如10~15秒）后再看看。
  - 你发送完了消息，对方还没有回应你 -> 可以先等一会儿。
  - 你觉得话题/事情好像自然结束了、不需要再做什么 -> 设置为平台全局等待（scope=platforms），并且设定一个较长的时间，这样平台上不管哪个会话来消息都能唤醒你。
在操作浏览器时：

  - 察觉页面或页面中的需要的图片、元素可能还在加载 -> 设定短等待时间，等待 browser（scope=browser）。

若 scope=world，则代表你同时等待两者。

## scope（监听范围）

- session：仅当前会话有新消息时触发（适合正在交谈中途的短暂等待）
- platforms：当前平台任意会话的消息均可触发（适合话题结束后的长时间静候，防止错过其他人的消息）
- browser：仅在浏览器开启时有效，当浏览器发生语义变化时触发
- world：整个<world>中有任意新情况时触发；包含社交平台和浏览器

## condition（触发条件）

- any_change：对于社交平台代表有任何新消息即触发；对于浏览器则代表浏览器页面有新变化触发。
- mentioned：仅针对社交平台有效，在被私聊、被 @ 或被回复时触发；注意当 scope=browser 时，mentioned 触发条件不可用。
"""
