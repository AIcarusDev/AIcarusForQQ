DESCRIPTION = """
重要的等待工具，可以让你暂时挂起等待。

适用场合示例：
  - 你察觉到对方似乎话没说完，后面可能还有新内容，适合先做倾听状 -> 考虑设定一个较短的等待时间（例如10~15秒）后再看看。
  - 你发送完了消息，对方还没有回应你 -> 可以先等一会儿。
  - 你觉得话题/事情好像自然结束了、不需要再做什么 -> 设置为全局等待（scope=global），并且设定一个较长的时间，这样不管哪个会话来消息都能唤醒你。

支持提前唤醒条件
  scope（监听范围）：
    - session：仅当前会话有新消息时触发（适合正在交谈中途的短暂等待）
    - global：任意会话的消息均可触发（适合话题结束后的长时间静候，防止错过其他人的消息）
  condition（触发条件）：
    - any_message：有任何新消息即触发
    - mentioned：仅在被 @ 或被回复时才触发（适合不想被普通消息打扰的场景）
"""